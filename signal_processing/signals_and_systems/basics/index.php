<p>
  Signal: Any physical quantity which varies with time, space or any other independent variable.
</p>
<p>
  Eg: i) A signal varying with time i.e. the values of the signal vary as a function of time.
  X(t) = 3*t
  ii) An equation of a plane i.e. a signal dependent on two independent variables (In this case the variables being the x and y co-ordinates).
</p>
<p>F(x,y) = x + 3xy + y^2</p>
<p>
  System: Any physical device which performs some operation on a signal.
