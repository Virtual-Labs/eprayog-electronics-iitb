<h1>Scilab</h1>
<p>
  Scilab is an open source, cross-platform numerical computational package and a high-level, numerically oriented programming language. It can be used for signal processing, statistical analysis, image enhancement, fluid dynamics simulations, numerical optimization, and modelling and simulation of explicit and implicit dynamical systems. 
</p>
<p>
  Scilab is an interpreted language with dynamically typed objects. Scilab runs, and is available in binary format, for the main available platforms: Unix/Linux workstations (the main software development is performed on Linux workstations), Windows, and MacOSX. Compiling Scilab from the source code is also possible and is fairly straightforward.
</p>
<p>
  Scilab offers quite a number of functions for signal processing i.e. it provides a number of features to implement signal processing applications. A collection of signal processing algorithms like convolution, fourier transforms, IIR filters, FIR filters etc have been implemented as functions in Scilab. For each signal processing tool available in the signal processing toolbox there is a tutorial section in the manual explaining the methodology behind the technique. Each signal processing subject is illustrated by examples and figures which were demonstrated using Scilab. To further assist the user, there exists for each example and figure an executable file which recreates the example or figure.
</p>

