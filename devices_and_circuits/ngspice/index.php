<h1>NGSPICE </h1>
</br>
<p>
NGSPICE is open source software tool widely used for simulation, originally written at The University of California at Berkeley (USA).
</p>
<p>
  NGSPICE is a widely used circuit simulator, which can be used to model and observe the behaviour of linear and nonlinear circuits. NGSPICE contains a library of basic circuit components and ideal/commonly used models of various semiconductor devices.
</p>
<p>
  NGSPICE supports mixed-level simulations which provides link between technology parameters and circuit performance with greater accuracy than a stand alone simulator, by numerically modeling the critical devices in a circuit.
</p>
